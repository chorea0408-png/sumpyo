/** 오늘 날짜 기준으로 하루 동안 고정되는 구절을 고른다 (자정에 다음 구절로 바뀜) */
export function verseOfTheDay(now: Date): Verse {
  const key = now.getFullYear() * 10000 + (now.getMonth() + 1) * 100 + now.getDate();
  return VERSES[key % VERSES.length];
}
